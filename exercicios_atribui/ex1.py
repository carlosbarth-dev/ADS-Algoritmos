# EXERCÍCIO 1 - Soma das faltas

# pedindo dados ao usuário
faltas1 = int(input("Faltas do 1º bimestre: "))
faltas2 = int(input("Faltas do 2º bimestre: "))

# somando os valores
total = faltas1 + faltas2

# exibindo resultado
print("Total de faltas:", total)

# OBS: int() porque faltas não tem número quebrado